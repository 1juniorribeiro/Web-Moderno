console.log('= é a atribuiçao para uma variavel receber, ter um valor atribuido');
const a = 7
let b = 3

b += a // b = b + a
console.log(b)

b -= a // b = b - a
console.log(b)

b *= a // b = b * a
console.log(b)

b /= a // b = b / a
console.log(b)

b %= a // b = b % a
console.log(b)

console.log('as operações são feitas e atribuidas a variavel escolhida')
